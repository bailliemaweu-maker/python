# students mark scenario 
marks = 55

if (marks >=0 and marks <=30) :
    print("Below Average")
elif (marks >30 and marks <=40) :
    print("Average")
elif (marks >40 and marks <=70) :
    print(" Above average")
elif (marks >70 and marks <=100) :
    print("Excellent")
else :
    print("Invalid Marks")