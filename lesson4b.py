# python for loops 
# loop from 1 to 10 
for index in range(1, 10, 1) :
     print(index)

# loop from 50 to 100

for number in range (50, 100, 1) :
     print(number)

# loop all even numbers from 0 to 100 

for count in range (0, 100, 2) :
     print(count)

# loop all years election has taken place in Kenya 
for years in range (1992, 2026, 5) :
     print(years)