# loop through a list 
languages =["english", "french", "german", "chinese", "swahili"]

for language in languages :
    print(language)

countries =["Kenya", "South Africa", "Mauritius", "Seychelles", "Egypt"]
 
for country in countries :
    print(country)

movies =["Top gun", "The Nun", "It", "Formula 1", "Scream", "The Crew"]

for movie in movies :
    print(movie)

artists =["Mf Doom", "Freddy Gibbz", "Joey BadAss", "Kanye West", "Lil_Wayne"]

for artist in artists :
    print(artist)